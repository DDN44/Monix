#include <stdker.h>
#include <irq.h>

void keyboard_out(uint8_t value)
{
    while((inb(0x64) & 0b10))
    {
        con_print("waiting for ps2" + 0x8E00);
        con_newln();
    }
    outb(0x60, value);
}

uint8_t keyboard_in()
{
    while((inb(0x64) & 0b01))
    {
        con_print("waiting for ps2" + 0x8E00);
        con_newln();
    }
    return inb(0x60);
}

void keyboard_test()
{
    outb(0x64, 0xAA);
    uint8_t contresult = inb(0x60);
    if(contresult = 0x55)
    {
        con_print("ps2 test passed:" + 0x8E00);
        con_print_hex32(contresult);
        con_newln;
    }else
    {
        con_print("ps2 test failed:" + 0x8E00);
        con_print_hex32(contresult);
        con_newln;
    }
}

